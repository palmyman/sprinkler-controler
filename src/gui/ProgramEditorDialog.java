    private void fillProgramComboBox() {
        try {
            programs = new ArrayList<>(ProgramDAO.getInstance().getAll());

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        programComboBox.removeAllItems();
        for (int i = 0; i < programs.size(); i++) {
            programComboBox.addItem(programs.get(i));
        }
    }

    private void fillPanelComboBox() {
        try {
            panels = new ArrayList<>(ControlPanelDAO.getInstance().getAll());

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
        panelComboBox.removeAllItems();
        for (int i = 0; i < panels.size(); i++) {
            panelComboBox.addItem(panels.get(i));
        }
    }

