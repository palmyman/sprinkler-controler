    private void rescheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rescheduleButtonActionPerformed
        Program program = new Program(
                programId,
                null,
                new java.sql.Date(this.dateValue.getDate().getTime()),
                new java.sql.Time(((java.util.Date) this.timeValue.getValue()).getTime()));
        try {
            ProgramDAO.getInstance().update(program);
        } catch (SQLException ex) {
            Logger.getLogger(ProgramEditorDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_rescheduleButtonActionPerformed

    private void selectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectButtonActionPerformed
        addButton.setEnabled(true);
        rescheduleButton.setEnabled(true);
        deleteProgramButton.setEnabled(true);
        deleteSprinklerButton.setEnabled(false);
        dateValue.setDate(((Program) programComboBox.getSelectedItem()).getDate());
        timeValue.setValue(((Program) programComboBox.getSelectedItem()).getTime());
        programId = ((Program) programComboBox.getSelectedItem()).getId();
        this.sprinklerTableModel.refresh(programId);
    }//GEN-LAST:event_selectButtonActionPerformed

